function fruitsBasket(basket) {
let fruitsBasket = {};
for (let index of basket) {
  if (fruitsBasket[index] === undefined) {
    fruitsBasket[index] = 1;
  } else {
    fruitsBasket[index] += 1;
  };
};
let messageArray = [];
for (let fruit in fruitsBasket) {
  let message = `${fruitsBasket[fruit]} ${fruit}`;
  if (fruitsBasket[fruit] > 1) {
    message += 's';
  };
  messageArray.push(message);
};
let finalMessage = `Sua cesta possui: ${messageArray.join(', ')}.`;
return finalMessage;
};
