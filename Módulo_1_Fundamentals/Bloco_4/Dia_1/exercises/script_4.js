//Testando variável com valor positivo

let a = 13;

if (a > 0) {
  console.log ("Positive")
} else if (a < 0) {
  console.log ("Negative")
} else {
  console.log ("Zero")
}

//Testando variável com valor negativo

a = -24;

if (a > 0) {
  console.log ("Positive")
} else if (a < 0) {
  console.log ("Negative")
} else {
  console.log ("Zero")
}

//Testando variável com valor zero

a = 0;

if (a > 0) {
  console.log ("Positive")
} else if (a < 0) {
  console.log ("Negative")
} else {
  console.log ("Zero")
}
